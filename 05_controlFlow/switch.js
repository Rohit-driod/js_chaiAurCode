const month = 'march'

switch (month) {
    case 'jan':
        console.log('January');
        break; 
    case 'feb':
        console.log('February');
        break; 
    case 'march':
        console.log('March');
        break; 
    case 'april':
        console.log('April');
        break; 

    default:
        console.log('error');
        break;
}
//impoortant //not using break executes all condition except default  

